import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { parse } from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const outputPath = path.join(repoRoot, "src", "fdicEndpointMetadata.ts");
const docsBaseUrl = "https://api.fdic.gov/banks/docs";

const endpointSources = {
  institutions: {
    path: "/institutions",
    propertyFile: "institution_properties.yaml",
  },
  locations: {
    path: "/locations",
    propertyFile: "location_properties.yaml",
  },
  history: {
    path: "/history",
    propertyFile: "history_properties.yaml",
  },
  summary: {
    path: "/summary",
    propertyFile: "summary_properties.yaml",
  },
  failures: {
    path: "/failures",
    propertyFile: "failure_properties.yaml",
  },
  sod: {
    path: "/sod",
    propertyFile: "sod_properties.yaml",
  },
  financials: {
    path: "/financials",
    propertyFile: "risview_properties.yaml",
  },
  demographics: {
    path: "/demographics",
    propertyFile: "demographics_properties.yaml",
  },
};

async function fetchText(relativePath) {
  const response = await fetch(`${docsBaseUrl}/${relativePath}`);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${relativePath}: HTTP ${response.status}`);
  }
  return response.text();
}

function normalizeProperties(data) {
  return data?.properties?.data?.properties ?? {};
}

function sanitizeText(value) {
  return typeof value === "string" ? value.replace(/\r\n/g, "\n") : undefined;
}

async function main() {
  const swagger = parse(await fetchText("swagger.yaml"));
  const metadata = {};

  for (const [endpoint, source] of Object.entries(endpointSources)) {
    const propertyDoc = parse(await fetchText(source.propertyFile));
    const fields = normalizeProperties(propertyDoc);
    const operation = swagger.paths?.[source.path]?.get;

    metadata[endpoint] = {
      endpoint,
      title: operation?.summary ?? endpoint,
      description: sanitizeText(operation?.description) ?? null,
      source: {
        swaggerPath: source.path,
        propertyFile: source.propertyFile,
        docsBaseUrl,
      },
      sortFields: Object.keys(fields).sort(),
      fields: Object.fromEntries(
        Object.entries(fields)
          .sort(([left], [right]) => left.localeCompare(right))
          .map(([fieldName, definition]) => [
            fieldName,
            {
              name: fieldName,
              title: sanitizeText(definition?.title) ?? null,
              description: sanitizeText(definition?.description) ?? null,
              type: definition?.type ?? null,
              filterable: true,
              sortable: true,
              output: true,
            },
          ]),
      ),
    };
  }

  const fileContents = `// Generated by scripts/sync-fdic-endpoint-metadata.mjs\n` +
    `// Source: https://api.fdic.gov/banks/docs\n\n` +
    `export interface FdicEndpointFieldMetadata {\n` +
    `  name: string;\n` +
    `  title: string | null;\n` +
    `  description: string | null;\n` +
    `  type: string | null;\n` +
    `  filterable: boolean;\n` +
    `  sortable: boolean;\n` +
    `  output: boolean;\n` +
    `}\n\n` +
    `export interface FdicEndpointMetadata {\n` +
    `  endpoint: string;\n` +
    `  title: string;\n` +
    `  description: string | null;\n` +
    `  source: {\n` +
    `    swaggerPath: string;\n` +
    `    propertyFile: string;\n` +
    `    docsBaseUrl: string;\n` +
    `  };\n` +
    `  sortFields: string[];\n` +
    `  fields: Record<string, FdicEndpointFieldMetadata>;\n` +
    `}\n\n` +
    `export const FDIC_ENDPOINT_METADATA: Record<string, FdicEndpointMetadata> = ${JSON.stringify(metadata, null, 2)};\n`;

  await fs.writeFile(outputPath, fileContents);
  console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
